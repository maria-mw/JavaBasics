package Java_Basics.Lesson_2.HW;

public class Calculator {

    //Addition
    public static Integer addition (Integer number1, Integer number2) {
        return number1 + number2;
    }
    public static Double addition (Double number1, Double number2) {
        return number1 + number2;
    }
    public static Long addition (Long number1, Long number2) {
        return number1 + number2;
    }

    //Subtraction
    public static Integer subtraction (Integer number1, Integer number2) {
        return number1 - number2;
    }
    public static Double subtraction (Double number1, Double number2) {
        return number1 - number2;
    }
    public static Long subtraction (Long number1, Long number2) {
        return number1 - number2;
    }

    //Multiplication
    public static Integer multiplication (Integer number1, Integer number2) {
        return number1 * number2;
    }
    public static Double multiplication (Double number1, Double number2) {
        return number1 * number2;
    }
    public static Long multiplication (Long number1, Long number2) {
        return number1 * number2;
    }

    //Division
    public static Double division (Integer number1, Integer number2) {
        return (double) number1 / number2;
    }
    public static Double division (Double number1, Double number2) {
        return number1 / number2;
    }
    public static Double division (Long number1, Long number2) {
        return (double) number1 / number2;
    }
}

